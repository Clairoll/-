function test(datas,superPath="") {
    for (let i =0; i<datas.length; i++){
        if(datas[i].indexRoute) {
            let url= ""
            if(superPath!== "") {
                routerList.push(superPath+"/"+datas[i].path)
                url = superPath+"/"+datas[i].path
            } else {
                url = datas[i].path
                routerList.push(datas[i].path)
            }

            if(datas[i].childRoutes) {
                test(datas[i].childRoutes,url)
            }
        } else {
            if(datas[i].childRoutes) {
                let url = ""
                if(superPath!== "") {
                    url = superPath+"/"+datas[i].path
                } else {
                    url = datas[i].path
                }
                test(datas[i].childRoutes,url)
            } else {
                if(superPath!== "") {
                    routerList.push(superPath+"/"+datas[i].path)
                } else {
                    routerList.push(datas[i].path)
                }
            }
        }
    }
    
}
