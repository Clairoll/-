let routerList = []
function flatRoutes(arr, superPath = '') {
    for (let i = 0; i < arr.length; i++) {
        const prefix = superPath && `${superPath}/`
        const toPath = (prefix && `${prefix}${arr[i].path}`) || arr[i].path

        if (arr[i].indexRoute || (!arr[i].indexRoute && !arr[i].childRoutes)) {
            routerList.push(toPath)
        }
        if (arr[i].childRoutes) {
            flatRoutes(arr[i].childRoutes, toPath)
        }
    }
}
